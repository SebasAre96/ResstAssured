package modelos;

public class AnimalAntiguo {
    private int id;
    private String nombre;
    private String tipo;
    private int edad;
    private double peso;
    private String genero;
    private AmoAntiguo amo;

    public int getId() {
        return id;
    }

    public String getNombre() {
        return nombre;
    }

    public String getTipo() {
        return tipo;
    }

    public int getEdad() {
        return edad;
    }

    public double getPeso() {
        return peso;
    }

    public String getGenero() {
        return genero;
    }

    public AmoAntiguo getAmo() {
        return amo;
    }
}
