package modelos;

public class Amo {
    private String nombre;
    private String apellido;
    private String correo;
    private int edad;
    private String pais;

    public String getNombre() {
        return nombre;
    }

    public String getApellido() {
        return apellido;
    }

    public String getCorreo() {
        return correo;
    }

    public int getEdad() {
        return edad;
    }

    public String getPais() {
        return pais;
    }
}
